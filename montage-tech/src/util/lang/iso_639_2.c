#define ISO_639_2_SUPPROT_MAX_DESC_CNT  \
  ((sizeof(iso_639_2_desc))/(sizeof(iso_639_2_desc_t)))

#define ISO_639_2_UNDEFINED_DESC       0x0

#define SWAP_BYTE_ORDER(x)             ((x >> 16) \
                                       | (x & 0x0000FF00) \
                                       | ((x & 0xFF) << 16))

static int compare_desc(const void *p_desc1, const void *p_desc2)
{
  return memcmp(p_desc1, p_desc2, ISO_639_2_DESC_LENGTH);
}

u16 iso_639_2_desc_to_idx(const char *p_desc)
{
  char *p_found = NULL;
  char *p_head = (char *)(iso_639_2_desc);

  CHECK_FAIL_RET((ISO_639_2_SUPPROT_MAX_DESC_CNT < 255), ISO_639_2_UNDEFINED_DESC);
  
  p_found = lib_bsearch(p_desc, 
                        iso_639_2_desc, 
                        ISO_639_2_SUPPROT_MAX_DESC_CNT, 
                        sizeof(iso_639_2_desc_t), 
                        compare_desc);
  if (p_found == NULL)
  {
    return ISO_639_2_UNDEFINED_DESC;
  }

  return (p_found - p_head)  / sizeof(iso_639_2_desc_t);
}

const char * iso_639_2_idx_to_desc(u16 idx)
{
  CHECK_FAIL_RET((ISO_639_2_SUPPROT_MAX_DESC_CNT < 255), ((char *)iso_639_2_desc[ISO_639_2_UNDEFINED_DESC].desc));
  
  if (idx >= ISO_639_2_SUPPROT_MAX_DESC_CNT
    || idx == ISO_639_2_UNDEFINED_DESC)
  {
    return (char *)iso_639_2_desc[ISO_639_2_UNDEFINED_DESC].desc;
  }

  return (char *)iso_639_2_desc[idx].desc;
}

u16 iso_639_2_idx_to_type(u16 idx)
{
  return iso_639_2_desc[idx].index;
}

